package Aula17.Ex.Atividade;

class Aluno3 {
    private String nome;
    private double notaFinal;

    public Aluno3(String nome, double notaFinal) {
        this.nome = nome;
        this.notaFinal = notaFinal;
    }

    public double getNotaFinal() {
        return notaFinal;
    }

    public String getNome() {
        return nome;
    }
}